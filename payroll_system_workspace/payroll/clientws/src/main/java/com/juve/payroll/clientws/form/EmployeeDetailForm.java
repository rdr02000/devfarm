package com.juve.payroll.clientws.form;

public class EmployeeDetailForm {
	private String employeeId;
	private String companyName;
	private String name;
	private String status;
	private String address;
	private String contactNumber;
	private String shiftSchedule;
	private String position;
	private Double dailyRate;
	private String taxId;
	private String sssId;
	private String hdmfId;
	
	public String getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getContactNumber() {
		return contactNumber;
	}
	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}
	public String getShiftSchedule() {
		return shiftSchedule;
	}
	public void setShiftSchedule(String shiftSchedule) {
		this.shiftSchedule = shiftSchedule;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public Double getDailyRate() {
		return dailyRate;
	}
	public void setDailyRate(Double dailyRate) {
		this.dailyRate = dailyRate;
	}
	public String getTaxId() {
		return taxId;
	}
	public void setTaxId(String taxId) {
		this.taxId = taxId;
	}
	public String getSssId() {
		return sssId;
	}
	public void setSssId(String sssId) {
		this.sssId = sssId;
	}
	public String getHdmfId() {
		return hdmfId;
	}
	public void setHdmfId(String hdmfId) {
		this.hdmfId = hdmfId;
	}
}
